#include <stdio.h>

int main(){
    int age = 4;
    
    if(age>10){
        printf("Your age is greater than 10 \n");
    }else{
        printf("Your age is lesser than 10 \n");
    }



    if(age%2==0){
        printf("your age is divisible by 2 \n");
    }
    else{
        printf("your age is not divisible by 2 \n");
    }

    return 0;
}

